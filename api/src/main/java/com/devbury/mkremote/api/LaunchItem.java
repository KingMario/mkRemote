package com.devbury.mkremote.api;

public class LaunchItem {

    public static final int DIR_TYPE = 1;
    public static final int FILE_TYPE = 2;
    private int type;
    private String name;
    private String path;
    private String icon;

    @Override
    public String toString() {
        return new StringBuffer().append("name: ").append(name).append(" type: ").append(type).append(" path: ").append(
                path).toString();
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }
}
